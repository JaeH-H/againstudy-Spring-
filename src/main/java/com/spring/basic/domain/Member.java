package com.spring.basic.domain;

public class Member {
    //속성
    private String name;
    private String email;
    private int age;

    //생성자

    //기능

    //Getter
    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public int getAge() {
        return age;
    }

    //Setter
    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
